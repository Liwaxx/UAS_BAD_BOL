    private void loadData(){
        conn = DBConnect.DBConnect();
        String sql = "SELECT * FROM barang";
       try {
           
           PStatement = conn.prepareStatement(sql);
           rs = PStatement.executeQuery();
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           
           while(rs.next()){
               String kategori = rs.getString("kategori");
               String kdBarang = rs.getString("Kd_Barang");
               String nmBarang = rs.getString("Nm_Barang");
               String satuan1 = rs.getString("satuan1");
               String satuan2 = rs.getString("satuan2");
               String satuan3 = rs.getString("satuan3");
               String satuan4 = rs.getString("satuan4");
               String satuan5 = rs.getString("satuan5");
               Object[] row = { kategori, kdBarang,nmBarang, satuan1, satuan2, satuan3, satuan4, satuan5 };
               model.addRow(row);
           }
           
       } catch(Exception ex) {
           System.out.println("Error: "+ex);
       }
    }

