    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        String kb = jTable1.getModel().getValueAt(row, 1).toString();
        
        String sql = "DELETE FROM barang WHERE Kd_Barang = '"+kb+"';";
        int dialogButton = JOptionPane.showConfirmDialog (null, "Yakin untuk menghapus barang?","WARNING",JOptionPane.YES_NO_OPTION);
        if(dialogButton == 0){
            try {
                  stmt = conn.createStatement();
                  if(stmt.executeUpdate(sql) == 1){
                    JOptionPane.showMessageDialog(null,"Success","Delete",JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(false);
                    new DaftarBarang().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null,"Opps?! Something went wrong","Delete",JOptionPane.WARNING_MESSAGE);
                }
           
            } catch(Exception ex) {
                System.out.println("Error: "+ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

